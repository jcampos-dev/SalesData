class Customer:
    def __init__(
        self,
        salesperson: str,
        first: str,
        last: str,
        address: str,
        city: str,
        state: str,
        zip: str,
        phone: str,
        email: str,
    ):
        self.salesperson: str = salesperson
        self.first: str = first
        self.last: str = last
        self.address: str = address
        self.city: str = city
        self.state: str = state
        self.zip: str = zip
        self.phone: str = phone
        self.email: str = email
